import {BrowserRouter,Routes,Route,Navigate} from 'react-router-dom';
import {useEffect,useState} from 'react';
import {auth} from './firebase';
import Dashboard from './pages/Dashboard';
import JoinGroup from './pages/JoinGroup';

function App(){
  const [user, setUser] = useState(null);
  const[loading, setLoading] = useState(true);

  //this will listen for any changes in the firebase auth state changes 
  useEffect(() => {
    const unsubscribe = auth.onAuthStateChanged((firebaseUser) => {
      setUser(firebaseUser);
      setLoading(false);
    });

    return () => unsubscribe();
  },[]);

  //this will wait for the firebase to check the authorisation first 
  if(loading){
    return <p style = {{textAlign : 'center',
                        marginTop : '35px',
    }}>Loading...</p>;
  }

  return(
    <BrowserRouter>
    <Routes>
      {/* redirect to dashboard when logged in otherwise user is prompted to login*/}
      <Route
        path = "/"
        element = {user ? <Navigate to= "/dashboard" /> : <Navigate to ="/login" />}
        />

        {/* dashboard can only be accessed when the user is logged in*/}
        <Route 
        path = "/dashboard"
        element = {user ? <Dashboard /> : <Navigate to ="/login"/>}
        />

        {/*joining group page can only be accessed when the user is logged in*/}
        <Route
        path = "/join"
      element = {user ? <JoinGroup/> :< Navigate to ="/login" />}
      />
    </Routes>
    </BrowserRouter>
  );
}

 export default App;








































